    private void AddBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBActionPerformed
        try {
            volume = Double.parseDouble(VolumeTF.getText());
            calories = Double.parseDouble(CaloriesTF.getText());
        } catch (NumberFormatException e) {
            logger.log("Invalid volume or calories");
            JOptionPane.showMessageDialog(this, "Invalid volume or calories", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Fruit ing = new Fruit(NameTF.getText(), calories, volume, color.getRed(), color.getGreen(), color.getBlue());
        try {
            blender.addIngredient(ing);
        } catch (BlenderOverFlowException ex) {
            logger.log("Blender overflow: " + ex.getMessage());
            JOptionPane.showMessageDialog(this, "Blender overflow", "Error", JOptionPane.ERROR_MESSAGE);
        }

        // Reset fields
        NameTF.setText("");
        CaloriesTF.setText("");
        VolumeTF.setText("");
        color = Color.WHITE;
        ColorPanel.setBackground(Color.WHITE);
        jPanel13.setVisible(false);
        jPanel22.setVisible(false);  
    }//GEN-LAST:event_AddBActionPerformed

