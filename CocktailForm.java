    private void BlendBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BlendBActionPerformed
        try {
            suger = new Suger("Sugar", Double.parseDouble(SugerCal.getText()), Double.parseDouble(SugerVol.getText()));
            milk = new Milk("Milk", Double.parseDouble(MilkCal.getText()), Double.parseDouble(MilkVol.getText()));
            blender.addIngredient(suger);
            blender.addIngredient(milk);
        } catch (NumberFormatException e) {
            logger.log("Invalid sugar or milk values");
            JOptionPane.showMessageDialog(this, "Invalid sugar or milk values", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } catch (BlenderOverFlowException ex) {
            logger.log("Blender overflow: " + ex.getMessage());
            JOptionPane.showMessageDialog(this, "Blender overflow", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        cocktail = blender.blend();
        try {
            blender.pourIntoCup(cup);
        } catch (BlenderEmptyException ex) {
            logger.log("Blender empty: " + ex.getMessage());
            JOptionPane.showMessageDialog(this, "Blender is empty", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        CocktailTextArea.setText(cocktail.getInfo());
        CupTextArea.setText(cup.getInfo());
        BlenderTextArea.setText(blender.getInfo());
        FinalColorPanel.setBackground(cocktail.getColor()); 
       jTabbedPane1.setSelectedIndex(2);
    }//GEN-LAST:event_BlendBActionPerformed

